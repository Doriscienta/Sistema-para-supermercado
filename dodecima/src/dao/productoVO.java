package dao;

public class productoVO {
String codigo;
String nombre;
String catego;
String provee;
public String getCodigo() {
	return codigo;
}
public void setCodigo(String codigo) {
	this.codigo = codigo;
}
public String getNombre() {
	return nombre;
}
public void setNombre(String nombre) {
	this.nombre = nombre;
}
public String getCatego() {
	return catego;
}
public void setCatego(String catego) {
	this.catego = catego;
}
public String getProvee() {
	return provee;
}
public void setProvee(String provee) {
	this.provee = provee;
}


}
